DATOS SEGMENT PARA 'DATA'
        LET1 DB 'ESTA ES UNA PRUEBA',0AH,0DH,'$'
DATOS ENDS

STACKSG SEGMENT PARA STACK 'STACK'
        DW 20 DUP(0)
STACKSG ENDS

CODIGO SEGMENT PARA 'CODE'
        ASSUME DS:DATOS,SS:STACKSG,CS:CODIGO,ES:DATOS

PRUEBA  PROC FAR

        PUSH DS
        XOR AX,AX
        PUSH AX

        MOV AX,DATOS
        MOV DS,AX

        MOV AH,09
        LEA DX,LET1
        INT 21H


        MOV AH,04CH
        MOV AL,00H
        INT 21H
       
PRUEBA ENDP
CODIGO ENDS
     END PRUEBA
